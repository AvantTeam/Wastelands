namespace wastelands.src.graphics
{
    public class AtlasRegion
    {
        public int x;
        public int y;
        public int width;
        public int height;

        public AtlasRegion(int x, int y, int width, int height)
        {
            this.x = x;
            this.y = y;
            this.width = width;
            this.height = height;
        }
    }
}
